# Operações com Strings: Dada a string "Python é incrível!", faça o seguinte:
frase = "Python é incrível!"

# Conte quantos caracteres ela possui (incluindo espacos)
print(len(frase))

# Converta toda a string para maiúsculas
print(frase.upper())

# Substitua a palavra ”incrível” por ”poderoso”
frase2 = frase.replace("incrível" , "poderoso")
print(frase2)