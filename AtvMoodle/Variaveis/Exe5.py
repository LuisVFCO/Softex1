# Tuplas e Conjuntos: Dada a tupla cores = ("vermelho", "verde", "azul", "verde"):
tupla = ("vermelho", "verde", "azul", "verde")

# Converta-a em um conjunto para remover duplicatas
novaTupla = set(tupla)

# Adicione a cor "amarelo" ao conjunto
novaTupla.add("amarelo")

print(tupla)
print(novaTupla)